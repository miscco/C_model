/****************************************************************************************************/
/*										Evaluation of SRK4											*/
/****************************************************************************************************/
void ODE(Cortical_Column& Col) {
	// first calculating every ith RK moment
	// has to be in order, 1th moment first

	for (int i=1; i<=4; ++i) {
		Col.set_RK(i);
	}

	// Adding all moments up
	Col.add_RK();

}
